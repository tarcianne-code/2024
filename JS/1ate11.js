// Inicializando a variável para armazenar a soma
let soma = 0;

// Usando um laço for para somar os números de 1 até 11
for (let i = 1; i <= 11; i++) {
    soma += i; // Adiciona o valor de i à soma
}

// Exibindo o resultado no console
console.log("A soma de todos os números de 1 até 11 é: " + soma);
